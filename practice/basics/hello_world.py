# Problem Statement:
# Write a Python program that takes a name as input and prints a greeting message.
#
# Intuition:
# Store the user-provided name in a variable and use it to build the greeting string.
#
# Input:
name = input("Enter your name: ")

# Logic:
# Create a greeting message using the entered name.
greeting = f"Hello, {name}!"

# Output:
print(greeting)
